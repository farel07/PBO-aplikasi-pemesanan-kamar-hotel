    private void btn_lihatpesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lihatpesananActionPerformed
        // TODO add your handling code here:

        int selectedRow = table_transaksi.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih transaksi terlebih dahulu!");
        return;
    }

    int idTransaksi = (int) table_transaksi.getValueAt(selectedRow, 0); // Kolom 0 = ID Transaksi

    List<Object[]> detailList = manager.getDetailTransaksi(idTransaksi);

    DefaultTableModel detailModel = new DefaultTableModel(
        new String[]{"ID Menu", "Nama Menu", "Qty", "Subtotal"}, 0
    );

    for (Object[] row : detailList) {
        detailModel.addRow(row);
    }

    table_detail.setModel(detailModel);

    }//GEN-LAST:event_btn_lihatpesananActionPerformed

