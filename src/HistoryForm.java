    private void tampilkanTransaksi() {
    DefaultTableModel model = new DefaultTableModel(
        new String[]{"ID Transaksi", "Tanggal", "Total", "ID Tamu"}, 0
    );
    table_transaksi.setModel(model);

    List<Object[]> data = manager.getSemuaTransaksi();
    for (Object[] row : data) {
        model.addRow(row);
    }
}

